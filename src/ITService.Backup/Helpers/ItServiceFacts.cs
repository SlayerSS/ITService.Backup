    /// <summary>Перемешанная колода: каждый факт по кругу, без повтора подряд между циклами.</summary>
    public sealed class FactRotator
    {
        private readonly string[] _deck;
        private int _position = -1;

        internal FactRotator(string[] facts)
        {
            if (facts.Length == 0)
            {
                _deck = [$"{CompanyInfo.Vendor} · {CompanyInfo.City}"];
                return;
            }

            _deck = new string[facts.Length];
            facts.CopyTo(_deck, 0);
            Shuffle(_deck);
        }

        public string Next()
        {
            if (_deck.Length == 1)
                return _deck[0];

            _position++;
            if (_position >= _deck.Length)
            {
                var previous = _deck[^1];
                Shuffle(_deck);
                if (_deck[0] == previous)
                    (_deck[0], _deck[1]) = (_deck[1], _deck[0]);
                _position = 0;
            }

            return _deck[_position];
        }

        private static void Shuffle(string[] array)
        {
            for (var i = array.Length - 1; i > 0; i--)
            {
                var j = Random.Shared.Next(i + 1);
                (array[i], array[j]) = (array[j], array[i]);
            }
        }
    }
